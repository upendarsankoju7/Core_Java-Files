package DataTypes;

public class Do_While_Loop {

    public static void main(String[] args) {
        // Initialize variable i with the value of 1
        int i = 1;

        // Start of the do-while loop
        // The loop will run until i becomes greater than 20
        do {
            // Print the value of i
            System.out.println(i); 

            // Increment the value of i by 1
            i++;
        } while (i <= 20); // The condition is checked after the loop body executes
    }
}
 